// Current language
let currentLanguage = 'en';

// Language switcher functionality
function initializeLanguageSwitcher() {
    const languageSelector = document.getElementById('language-selector');
    if (languageSelector) {
        languageSelector.addEventListener('change', function() {
            switchLanguage(this.value);
        });
    }
    
    // Check for saved language preference
    const savedLanguage = localStorage.getItem('preferredLanguage');
    if (savedLanguage && translations[savedLanguage]) {
        switchLanguage(savedLanguage);
        if (languageSelector) {
            languageSelector.value = savedLanguage;
        }
    }
}

function switchLanguage(languageCode) {
    if (!translations[languageCode]) {
        console.error('Language not supported:', languageCode);
        return;
    }
    
    currentLanguage = languageCode;
    localStorage.setItem('preferredLanguage', languageCode);
    
    // Update all translatable elements
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[languageCode][key]) {
            // Handle different element types
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[languageCode][key];
            } else {
                element.textContent = translations[languageCode][key];
            }
        }
    });
    
    // Update direction for RTL languages
    if (languageCode === 'ar') {
        document.body.setAttribute('dir', 'rtl');
        document.body.style.textAlign = 'right';
    } else {
        document.body.setAttribute('dir', 'ltr');
        document.body.style.textAlign = 'left';
    }
    
    // Trigger custom event for language change
    document.dispatchEvent(new CustomEvent('languageChanged', {
        detail: { language: languageCode }
    }));
}

function translate(key, language = currentLanguage) {
    return translations[language] && translations[language][key] ? translations[language][key] : key;
}

// Initialize language system when DOM is loaded
document.addEventListener('DOMContentLoaded', function() {
    initializeLanguageSwitcher();
});

// Export for use in other files
window.LanguageSystem = {
    translations,
    currentLanguage,
    switchLanguage,
    translate,
    initializeLanguageSwitcher
};